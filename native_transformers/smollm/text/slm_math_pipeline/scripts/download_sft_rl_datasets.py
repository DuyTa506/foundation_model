#!/usr/bin/env python3
"""
Pre-download all HuggingFace datasets needed for SFT, DPO (post-train), and GRPO/RL stages.

Reads from:
  - configs/datasets_en_vi_math_finetune.yaml   (SFT sources)
  - configs/datasets_en_vi_math_posttrain.yaml  (DPO sources)
  - configs/training_rl_grpo.yaml               (GRPO prompt datasets)

Skips:
  - Sources with hf_dataset: null (local files generated by synth_vi_reasoning.py)
  - DPO sources with enabled: false
  - GRPO vi_math_prompts that are local .jsonl paths

Usage:
    python scripts/download_sft_rl_datasets.py
    python scripts/download_sft_rl_datasets.py --dry_run
    python scripts/download_sft_rl_datasets.py --stages sft rl
    python scripts/download_sft_rl_datasets.py --cache_dir /data/hf_cache --hf_token hf_xxx
    python scripts/download_sft_rl_datasets.py --source_ids openr1_math numina_cot

Outputs:
    <cache_dir>/<dataset>/           HF dataset cache (arrow format)
    <cache_dir>/download_sft_rl_report.json
"""

from __future__ import annotations

import argparse
import json
import os
import time
from pathlib import Path

import yaml


APPROX_SIZES_GB = {
    # SFT — Vietnamese (5CD-AI translations)
    "5CD-AI/Viet-qvq-r1": 3,
    "5CD-AI/Vietnamese-395k-meta-math-MetaMathQA-gg-translated": 1,
    "5CD-AI/Vietnamese-microsoft-orca-math-word-problems-200k-gg-translated": 1,
    "5CD-AI/Vietnamese-nvidia-OpenMathInstruct-1-50k-gg-translated": 0.3,
    "5CD-AI/Vietnamese-argilla-OpenHermesPreferences-66k-gg-translated": 0.5,
    # SFT — English thinking
    "open-r1/OpenR1-Math-220k": 2,
    "AI-MO/NuminaMath-CoT": 0.5,
    "AI-MO/NuminaMath": 1,
    # SFT — English non-thinking
    "meta-math/MetaMathQA": 0.5,
    "hkust-nlp/dart-math-pool-math": 0.3,
    "derek-thomas/ScienceQA": 0.2,
    "allenai/sciq": 0.05,
    # DPO SFT fallback
    "agicorp/MathInstruct": 0.5,
    # GRPO
    "openai/gsm8k": 0.05,
    "hendrycks/competition_math": 0.2,
    "open-r1/DAPO-Math-17k": 0.1,
}


# ── Source parsers (each config has a different schema) ───────────────────────

def parse_sft_sources(cfg: dict) -> list[dict]:
    """Parse datasets_en_vi_math_finetune.yaml — flat sources list."""
    out = []
    for src in cfg.get("sources", []):
        hf = src.get("hf_dataset")
        if not hf:
            continue  # local file (synth output)
        out.append({
            "source_id": src["id"],
            "stage": "sft",
            "hf_dataset": hf,
            "subset": src.get("subset"),
            "split": src.get("split", "train"),
            "language": src.get("language", "?"),
            "mode": src.get("mode", "?"),
        })
    return out


def parse_dpo_sources(cfg: dict) -> list[dict]:
    """Parse datasets_en_vi_math_posttrain.yaml — nested preference_pairs + sft_fallback."""
    out = []
    sources = cfg.get("sources", {})

    for src in sources.get("preference_pairs", []):
        if not src.get("enabled", True):
            continue  # skip disabled DPO pairs
        hf = src.get("hf_dataset")
        if not hf:
            continue
        out.append({
            "source_id": src["id"],
            "stage": "dpo",
            "hf_dataset": hf,
            "subset": src.get("subset"),
            "split": src.get("split", "train"),
            "language": src.get("language", "?"),
            "mode": "preference_pair",
        })

    for src in sources.get("sft_fallback", []):
        hf = src.get("hf_dataset")
        if not hf:
            continue
        out.append({
            "source_id": src["id"],
            "stage": "dpo_sft_fallback",
            "hf_dataset": hf,
            "subset": src.get("subset"),
            "split": src.get("split", "train"),
            "language": src.get("language", "?"),
            "mode": "no_think",
        })

    return out


def parse_rl_sources(cfg: dict) -> list[dict]:
    """Parse training_rl_grpo.yaml — data.en_math_prompts + data.vi_math_prompts."""
    out = []
    data = cfg.get("data", {})

    for entry in data.get("en_math_prompts", []):
        entry = str(entry)
        if Path(entry).suffix in (".jsonl", ".json", ".txt"):
            continue  # local file
        out.append({
            "source_id": entry.replace("/", "__"),
            "stage": "rl",
            "hf_dataset": entry,
            "subset": None,
            "split": "train",
            "language": "en",
            "mode": "prompt_only",
        })

    for entry in data.get("vi_math_prompts", []):
        entry = str(entry)
        if Path(entry).suffix in (".jsonl", ".json", ".txt") or entry.startswith("outputs/"):
            continue  # local synth file — skip
        out.append({
            "source_id": entry.replace("/", "__"),
            "stage": "rl",
            "hf_dataset": entry,
            "subset": None,
            "split": "train",
            "language": "vi",
            "mode": "prompt_only",
        })

    return out


def dedup_sources(sources: list[dict]) -> list[dict]:
    """
    Deduplicate by (hf_dataset, subset, split). When the same dataset appears in
    multiple stages, keep the first occurrence but note all stages it's used in.
    """
    seen: dict[tuple, dict] = {}
    for src in sources:
        key = (src["hf_dataset"], src.get("subset"), src["split"])
        if key in seen:
            existing_stage = seen[key]["stage"]
            this_stage = src["stage"]
            if this_stage not in existing_stage:
                seen[key]["stage"] = f"{existing_stage}+{this_stage}"
        else:
            seen[key] = dict(src)
    return list(seen.values())


# ── Downloader ────────────────────────────────────────────────────────────────

def download_source(src: dict, cache_dir: Path | None, hf_token: str | None) -> dict:
    from datasets import load_dataset

    hf_dataset = src["hf_dataset"]
    subset = src.get("subset")
    split = src["split"]

    stage_label = src["stage"]
    print(f"\n[{src['source_id']}] ({stage_label}) {hf_dataset}"
          f"{f' ({subset})' if subset else ''} split={split}")

    approx = APPROX_SIZES_GB.get(hf_dataset)
    if approx:
        print(f"  estimated size: ~{approx} GB")

    t0 = time.time()
    status = {
        "source_id": src["source_id"],
        "stage": stage_label,
        "hf_dataset": hf_dataset,
        "subset": subset,
        "split": split,
    }

    try:
        load_kwargs = dict(
            path=hf_dataset,
            split=split,
            cache_dir=str(cache_dir) if cache_dir else None,
            token=hf_token,
            streaming=False,
        )
        if subset:
            load_kwargs["name"] = subset

        ds = load_dataset(**load_kwargs)
        elapsed = time.time() - t0
        status.update({"status": "ok", "rows": len(ds), "elapsed_s": round(elapsed, 1)})
        print(f"  done: {len(ds):,} rows in {elapsed:.0f}s")
    except Exception as e:
        elapsed = time.time() - t0
        status.update({"status": "error", "error": str(e), "elapsed_s": round(elapsed, 1)})
        print(f"  ERROR: {e}")

    return status


# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Pre-download HF datasets for SFT, DPO, and GRPO/RL stages."
    )
    parser.add_argument("--sft_config",
                        default="configs/datasets_en_vi_math_finetune.yaml")
    parser.add_argument("--dpo_config",
                        default="configs/datasets_en_vi_math_posttrain.yaml")
    parser.add_argument("--rl_config",
                        default="configs/training_rl_grpo.yaml")
    parser.add_argument(
        "--stages", nargs="+",
        choices=["sft", "dpo", "rl"],
        default=["sft", "dpo", "rl"],
        help="Which stages to download. Default: all three.",
    )
    parser.add_argument("--cache_dir", default=None,
                        help="Local HF cache directory. Defaults to HF_HOME.")
    parser.add_argument("--hf_token", default=os.environ.get("HF_TOKEN"),
                        help="HuggingFace token (or set HF_TOKEN env var).")
    parser.add_argument("--source_ids", nargs="+", default=None,
                        help="Download only these source_ids. Omit to download all.")
    parser.add_argument("--dry_run", action="store_true",
                        help="Print sources without downloading.")
    args = parser.parse_args()

    all_sources: list[dict] = []

    if "sft" in args.stages:
        with open(args.sft_config, "r", encoding="utf-8") as f:
            sft_cfg = yaml.safe_load(f)
        sft_sources = parse_sft_sources(sft_cfg)
        print(f"[sft]  {len(sft_sources)} HF sources (skipped local synth files)")
        all_sources.extend(sft_sources)

    if "dpo" in args.stages:
        with open(args.dpo_config, "r", encoding="utf-8") as f:
            dpo_cfg = yaml.safe_load(f)
        dpo_sources = parse_dpo_sources(dpo_cfg)
        print(f"[dpo]  {len(dpo_sources)} HF sources (skipped enabled=false sources)")
        all_sources.extend(dpo_sources)

    if "rl" in args.stages:
        with open(args.rl_config, "r", encoding="utf-8") as f:
            rl_cfg = yaml.safe_load(f)
        rl_sources = parse_rl_sources(rl_cfg)
        print(f"[rl]   {len(rl_sources)} HF sources (skipped local vi_math_prompts.jsonl)")
        all_sources.extend(rl_sources)

    all_sources = dedup_sources(all_sources)

    if args.source_ids:
        all_sources = [s for s in all_sources if s["source_id"] in args.source_ids]
        if not all_sources:
            print(f"\nNo sources matched {args.source_ids}.")
            return

    if not all_sources:
        print("No HF sources to download.")
        return

    total_approx = sum(APPROX_SIZES_GB.get(s["hf_dataset"], 0) for s in all_sources)
    print(f"\nTotal sources: {len(all_sources)}  (~{total_approx:.1f} GB estimated)\n")

    col_w = max(len(s["source_id"]) for s in all_sources)
    for s in all_sources:
        subset_str = f" [{s['subset']}]" if s.get("subset") else ""
        size_str = f"  ~{APPROX_SIZES_GB[s['hf_dataset']]} GB" if s["hf_dataset"] in APPROX_SIZES_GB else ""
        stage_str = s["stage"]
        print(f"  [{stage_str:18s}]  {s['source_id']:{col_w}s}  "
              f"{s['hf_dataset']}{subset_str}{size_str}")

    if args.dry_run:
        print("\n--dry_run: nothing downloaded.")
        return

    if not args.hf_token:
        print(
            "\nWARN: no HF token. Some datasets may require authentication.\n"
            "Set HF_TOKEN env var or pass --hf_token hf_xxx\n"
        )

    try:
        import datasets  # noqa: F401
    except ImportError:
        raise SystemExit("Missing: pip install datasets")

    cache_dir = Path(args.cache_dir) if args.cache_dir else None
    if cache_dir:
        cache_dir.mkdir(parents=True, exist_ok=True)

    report = []
    failed = []
    t_total = time.time()

    for src in all_sources:
        result = download_source(src, cache_dir, args.hf_token)
        report.append(result)
        if result["status"] == "error":
            failed.append(src["source_id"])

    elapsed_total = time.time() - t_total
    print(f"\n{'='*60}")
    print(f"Done: {len(all_sources) - len(failed)}/{len(all_sources)} sources "
          f"in {elapsed_total/60:.1f} min")
    if failed:
        print(f"Failed: {failed}")

    report_path = (cache_dir or Path(".")) / "download_sft_rl_report.json"
    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(
            {"sources": report, "total_elapsed_s": round(elapsed_total, 1)},
            f, indent=2, ensure_ascii=False,
        )
    print(f"Report: {report_path}")


if __name__ == "__main__":
    main()
